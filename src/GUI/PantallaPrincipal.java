    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String nombre = JOptionPane.showInputDialog("Inserte el registro que desea eliminar");

        if (nombre == null || nombre.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "No ingresaste ninguna ciudad.", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }

        List<Registro> lista = LogicaRegistro.getRegistro();
        Registro operacionAeliminar = null;

        for (Registro h : lista) {
            if (h.getCiudad().equalsIgnoreCase(nombre.trim())) {
                operacionAeliminar = h;
                break;
            }
        }

        if (operacionAeliminar != null) {

            int respuesta = JOptionPane.showConfirmDialog(
                    this,
                    "Seguro?",
                    "",
                    JOptionPane.YES_NO_OPTION
            );
            if (respuesta == JOptionPane.YES_OPTION) {
                lista.remove(operacionAeliminar);
                JOptionPane.showMessageDialog(this, "de acuerdo eliminamos la visita a '" + nombre);
                refrescar();
            } else if(respuesta == JOptionPane.NO_OPTION) {
            
                            JOptionPane.showMessageDialog(this, "sabia que no te querias perder la visita a "+nombre);

            
            }else {
                JOptionPane.showMessageDialog(this, "No se encontró ningún registro con el nombre '" + nombre + "'.", "Error", JOptionPane.ERROR_MESSAGE);
            }

        }


    }//GEN-LAST:event_jButton1ActionPerformed

