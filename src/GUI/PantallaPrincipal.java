    private void refrescar() {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();

        model.setRowCount(0);

        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yy");
        java.net.URL imageUrl = null;
        javax.swing.ImageIcon icono = null;
        for (Registro nombre : LogicaRegistro.getRegistro()) {
            model.addRow(new Object[]{
                nombre.getCiudad(),
                sdf.format(nombre.getFecha()),
                nombre.getTemporada(),
                nombre.getTminima(),
                nombre.getTmaxima(),
                nombre.getMedia()

            });

            if (nombre.getTemporada().equals("invierno")) {

                imageUrl = getClass().getResource("src/IMGS/invierno.png");

            } else if (nombre.getTemporada().equals("verano")) {
                imageUrl = getClass().getResource("src/IMGS/verano.jpg");

            } else if (nombre.getTemporada().equals("primavera")) {

                imageUrl = getClass().getResource("src/IMGS/primavera.png");

            } else if (nombre.getTemporada().equals("otoño")) {
                imageUrl = getClass().getResource("src/IMGS/otono.png");

            }

            if (imageUrl != null) {

                icono = new javax.swing.ImageIcon(imageUrl);
                java.awt.Image imgEscalada = icono.getImage().getScaledInstance(
                        jLabel1.getWidth(),
                        jLabel1.getHeight(),
                        java.awt.Image.SCALE_SMOOTH);
                jLabel1.setIcon(new javax.swing.ImageIcon(imgEscalada));
                jLabel1.setText("");
            } else {

                jLabel1.setIcon(null);
                jLabel1.setText("Imagen no encontrada");
            }

        }

    }

